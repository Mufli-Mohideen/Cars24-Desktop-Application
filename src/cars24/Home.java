    private void createPieChart() {
        
        
        int bodyWashCount = 0;
        int waxCount = 0;
        int interiorCleanCount = 0;
        int engineOilChangeCount = 0;
        int gearOilChangeCount = 0;
        
        try {
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cars24eaddb", "root", "");
            Statement statement = con.createStatement();
            
            
            // Query for Body Wash
            ResultSet resultSetBodyWash = statement.executeQuery("SELECT COUNT(*) AS count FROM tblvehiclestat WHERE bodywash = 'Required'");
            if (resultSetBodyWash.next()) {
                bodyWashCount = resultSetBodyWash.getInt("count");
            }
            
            // Query for Wax
            ResultSet resultSetWax = statement.executeQuery("SELECT COUNT(*) AS count FROM tblvehiclestat WHERE wax = 'Required'");
            if (resultSetWax.next()) {
                waxCount = resultSetWax.getInt("count");
            }
            
            // Query for Interior Clean
            ResultSet resultSetInteriorClean = statement.executeQuery("SELECT COUNT(*) AS count FROM tblvehiclestat WHERE interiorclean = 'Required'");
            if (resultSetInteriorClean.next()) {
                interiorCleanCount = resultSetInteriorClean.getInt("count");
            }
            
            // Query for Engine Oil Change

            ResultSet resultSetEngineOilChange = statement.executeQuery("SELECT COUNT(*) AS count FROM tblvehiclestat WHERE engineoil = 'Required'");
            if (resultSetEngineOilChange.next()) {
                engineOilChangeCount = resultSetEngineOilChange.getInt("count");
            }
            
            // Query for Gear Oil Change
            ResultSet resultSetGearOilChange = statement.executeQuery("SELECT COUNT(*) AS count FROM tblvehiclestat WHERE gearoil = 'Required'");
            if (resultSetGearOilChange.next()) {
                gearOilChangeCount = resultSetGearOilChange.getInt("count");
            }
            
            
            
            // Close the database connection
            statement.close();
            resultSetBodyWash.close();
            resultSetWax.close();
            resultSetInteriorClean.close();
            resultSetEngineOilChange.close();
            resultSetGearOilChange.close();

        

        // Create the pie chart
        DefaultPieDataset pie = new DefaultPieDataset();
        pie.setValue("Body Wash", bodyWashCount);
        pie.setValue("Wax",waxCount);
        pie.setValue("Interior Clean",interiorCleanCount);
        pie.setValue("Engine Oil Change",engineOilChangeCount);
        pie.setValue("Gear Oil Change",gearOilChangeCount);
        
        
        JFreeChart chart = ChartFactory.createPieChart3D("Pie Chart", pie,true,true,true);
        PiePlot3D P = (PiePlot3D) chart.getPlot();
        
        P.setSectionPaint("Body Wash", Color.RED);
        P.setSectionPaint("Wax", Color.GREEN);
        P.setSectionPaint("Interior Clean", Color.BLUE);
        P.setSectionPaint("Engine Oil Change", Color.ORANGE);
        P.setSectionPaint("Gear Oil Change", Color.YELLOW);
        
        
        P.setForegroundAlpha(1.0f);
        ChartFrame frame = new ChartFrame("PC",chart);
        frame.setVisible(true);
        frame.setSize(450,500);
        frame.setLocationRelativeTo(null);
        
        }
        
        
        catch (Exception e) {
            e.printStackTrace();
        }
        // Create chart panel and add it to the UI
//        ChartPanel chartPanel = new ChartPanel(pieChart);
//        chartPanel.setPreferredSize(new Dimension(600, 400));
//        this.chartPanel.removeAll();
//        this.chartPanel.add(chartPanel, BorderLayout.CENTER);
//        this.chartPanel.validate();
    }
    
